CREATE TABLE course (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1, INCREMENT BY 1) PRIMARY KEY,
  name_course VARCHAR(100),
  id_course  VARCHAR(100),
  tiet VARCHAR(20),
  si_so INTEGER ,
  giao_vien VARCHAR(100),
  phong_hoc VARCHAR(100),
  so_tin_chi INTEGER,
  ghi_chu VARCHAR(500)
);